function F = est_optimal_affine(point1,point2)
A=[point1(1,1),point1(1,2),1,0,0,0;...
    0,0,0,point1(1,1),point1(1,2),1;...
    point1(2,1),point1(2,2),1,0,0,0;...
    0,0,0,point1(2,1),point1(2,2),1;...
    point1(3,1),point1(3,2),1,0,0,0;...
    0,0,0,point1(3,1),point1(3,2),1
    ];
b=[point2(1,1);point2(1,2);point2(2,1);point2(2,2);point2(3,1);point2(3,2)];
F=A\b;
F=[F(1:3)';F(4:6)'];
end